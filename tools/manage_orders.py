def cmd_summary(args):
    """Show PO pipeline statistics."""
    session = SessionLocal()
    try:
        all_orders = session.query(PurchaseOrder).all()

        if not all_orders:
            logger.info("No purchase orders in the system.")
            return

        # Count by status
        status_counts = {}
        status_costs = {}
        for status in VALID_STATUSES:
            status_counts[status] = 0
            status_costs[status] = Decimal("0")

        total_cost_all = Decimal("0")
        total_qty_all = 0

        now = datetime.utcnow()
        week_end = now + timedelta(days=7)
        deliveries_this_week = []

        for po in all_orders:
            status_counts[po.status] = status_counts.get(po.status, 0) + 1
            cost = Decimal(str(po.total_cost)) if po.total_cost else Decimal("0")
            status_costs[po.status] = status_costs.get(po.status, Decimal("0")) + cost
            total_cost_all += cost
            total_qty_all += po.quantity or 0

            # Check for expected deliveries this week (for non-terminal POs)
            if po.status in ("pending", "confirmed", "shipped"):
                if po.expected_delivery and now <= po.expected_delivery <= week_end:
                    deliveries_this_week.append(po)

        print("\n" + "=" * 60)
        print("  PURCHASE ORDER SUMMARY")
        print("=" * 60)

        print(f"\n  Total Orders: {len(all_orders)}")
        print(f"  Total Units:  {total_qty_all}")
        print(f"  Total Cost:   {format_currency(total_cost_all)}")

        print(f"\n  {'Status':<14} {'Count':>6} {'Total Cost':>14}")
        print(f"  {'-'*14} {'-'*6} {'-'*14}")
        for status in VALID_STATUSES:
            count = status_counts[status]
            cost = status_costs[status]
            print(f"  {status:<14} {count:>6} {format_currency(cost):>14}")

        # Active pipeline (pending + confirmed + shipped)
        active_count = status_counts["pending"] + status_counts["confirmed"] + status_counts["shipped"]
        active_cost = status_costs["pending"] + status_costs["confirmed"] + status_costs["shipped"]
        print(f"\n  Active Pipeline: {active_count} orders, {format_currency(active_cost)} total")

        # Deliveries this week
        print(f"\n  Expected Deliveries This Week: {len(deliveries_this_week)}")
        if deliveries_this_week:
            for po in deliveries_this_week:
                print(
                    f"    - {po.po_id}  ASIN: {po.asin}  "
                    f"Qty: {po.quantity}  ETA: {format_date(po.expected_delivery)}  "
                    f"Status: {po.status}"
                )

        print("\n" + "=" * 60 + "\n")
    finally:
        session.close()


