def cmd_receive(args):
    """
    Mark a purchase order as received.
    Updates inventory stock levels and supplier on-time delivery rate.
    """
    session = SessionLocal()
    try:
        po = session.query(PurchaseOrder).filter(PurchaseOrder.po_id == args.po_id).first()
        if not po:
            logger.error(f"Purchase order {args.po_id} not found.")
            return

        if po.status not in ("confirmed", "shipped"):
            logger.error(
                f"Cannot receive PO {args.po_id}: current status is '{po.status}' "
                f"(must be 'confirmed' or 'shipped')."
            )
            return

        now = datetime.utcnow()

        # Update PO status
        po.status = "received"
        po.actual_delivery = now
        po.last_updated = now

        # --- Update Inventory ---
        inventory = session.query(Inventory).filter(Inventory.asin == po.asin).first()
        if inventory:
            inventory.current_stock = (inventory.current_stock or 0) + po.quantity
            inventory.available = (inventory.available or 0) + po.quantity
            inventory.last_restock_date = now
            inventory.last_updated = now

            # Recalculate needs_reorder
            reorder_point = inventory.reorder_point or 0
            inventory.needs_reorder = inventory.current_stock <= reorder_point

            logger.info(f"  Inventory updated for {po.asin}:")
            logger.info(f"    Current stock: {inventory.current_stock}")
            logger.info(f"    Available:     {inventory.available}")
            logger.info(f"    Needs reorder: {inventory.needs_reorder}")
        else:
            # Create inventory record if it doesn't exist
            inventory = Inventory(
                asin=po.asin,
                current_stock=po.quantity,
                reserved=0,
                available=po.quantity,
                last_restock_date=now,
                needs_reorder=False,
            )
            session.add(inventory)
            logger.info(f"  Created new inventory record for {po.asin} with {po.quantity} units.")

        # --- Update Supplier Reliability ---
        supplier = session.query(Supplier).filter(Supplier.supplier_id == po.supplier_id).first()
        if supplier:
            # Increment total orders
            old_total = supplier.total_orders or 0
            supplier.total_orders = old_total + 1

            # Determine if delivery was on time
            on_time = True
            if po.expected_delivery is not None:
                on_time = now <= po.expected_delivery

            # Update on_time_delivery_rate as running average
            old_rate = supplier.on_time_delivery_rate if supplier.on_time_delivery_rate is not None else 1.0
            if old_total > 0:
                # Weighted running average: ((old_rate * old_total) + new_value) / new_total
                supplier.on_time_delivery_rate = (
                    (old_rate * old_total) + (1.0 if on_time else 0.0)
                ) / supplier.total_orders
            else:
                supplier.on_time_delivery_rate = 1.0 if on_time else 0.0

            supplier.last_order_date = now
            supplier.last_updated = now

            delivery_status = "ON TIME" if on_time else "LATE"
            logger.info(f"  Supplier '{supplier.name}' updated:")
            logger.info(f"    Delivery: {delivery_status}")
            logger.info(f"    Total orders:         {supplier.total_orders}")
            logger.info(f"    On-time delivery rate: {supplier.on_time_delivery_rate:.1%}")

        session.commit()

        logger.info(f"Purchase order {args.po_id} received successfully.")
        logger.info(f"  ASIN:            {po.asin}")
        logger.info(f"  Quantity:        {po.quantity}")
        logger.info(f"  Actual Delivery: {format_date(po.actual_delivery)}")
    finally:
        session.close()


