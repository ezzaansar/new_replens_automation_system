    confirm_parser = subparsers.add_parser("confirm", help="Confirm a pending purchase order")
    confirm_parser.add_argument("po_id", type=str, help="Purchase order ID")

    # ship
    ship_parser = subparsers.add_parser("ship", help="Mark a purchase order as shipped")
    ship_parser.add_argument("po_id", type=str, help="Purchase order ID")

