def cmd_create(args):
    """Create a new purchase order for a product, picking the best supplier."""
    session = SessionLocal()
    try:
        # Validate product exists
        product = session.query(Product).filter(Product.asin == args.asin).first()
        if not product:
            logger.error(f"Product {args.asin} not found in database.")
            return

        # Find best supplier for this product
        # Prefer: is_preferred first, then lowest total_cost, then highest reliability
        product_suppliers = (
            session.query(ProductSupplier)
            .join(Supplier, ProductSupplier.supplier_id == Supplier.supplier_id)
            .filter(
                ProductSupplier.asin == args.asin,
                ProductSupplier.status == "active",
                Supplier.status == "active",
            )
            .order_by(
                ProductSupplier.is_preferred.desc(),
                ProductSupplier.total_cost.asc(),
            )
            .all()
        )

        if not product_suppliers:
            logger.error(f"No active suppliers found for ASIN {args.asin}. Add suppliers first.")
            return

        best_ps = product_suppliers[0]
        supplier = session.query(Supplier).filter(Supplier.supplier_id == best_ps.supplier_id).first()

        # Validate quantity against minimum order
        if supplier.min_order_qty and args.quantity < supplier.min_order_qty:
            logger.warning(
                f"Quantity {args.quantity} is below supplier minimum order quantity "
                f"of {supplier.min_order_qty}. Adjusting to minimum."
            )
            args.quantity = supplier.min_order_qty

        # Calculate costs from real supplier data
        unit_cost = best_ps.total_cost if best_ps.total_cost else best_ps.supplier_cost
        if unit_cost is None:
            logger.error(f"No cost data available for supplier {supplier.name} on ASIN {args.asin}.")
            return

        total_cost = Decimal(args.quantity) * Decimal(str(unit_cost))

        # Calculate expected delivery
        lead_time = supplier.lead_time_days if supplier.lead_time_days else 7
        expected_delivery = datetime.utcnow() + timedelta(days=lead_time)

        # Generate PO
        po_id = generate_po_id()
        po = PurchaseOrder(
            po_id=po_id,
            asin=args.asin,
            supplier_id=supplier.supplier_id,
            quantity=args.quantity,
            unit_cost=unit_cost,
            total_cost=total_cost,
            status="pending",
            order_date=datetime.utcnow(),
            expected_delivery=expected_delivery,
            notes=f"Manually created via manage_orders tool",
        )
        session.add(po)
        session.commit()

        logger.info(f"Purchase order created successfully:")
        logger.info(f"  PO ID:             {po_id}")
        logger.info(f"  ASIN:              {args.asin}")
        logger.info(f"  Product:           {product.title[:60] if product.title else 'N/A'}")
        logger.info(f"  Supplier:          {supplier.name} (ID: {supplier.supplier_id})")
        logger.info(f"  Quantity:          {args.quantity}")
        logger.info(f"  Unit Cost:         {format_currency(unit_cost)}")
        logger.info(f"  Total Cost:        {format_currency(total_cost)}")
        logger.info(f"  Expected Delivery: {format_date(expected_delivery)}")
    finally:
        session.close()


