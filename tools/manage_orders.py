def cmd_list(args):
    """List purchase orders with optional filtering."""
    session = SessionLocal()
    try:
        query = session.query(PurchaseOrder)

        if args.status:
            if args.status not in VALID_STATUSES:
                logger.error(f"Invalid status '{args.status}'. Must be one of: {', '.join(VALID_STATUSES)}")
                return
            query = query.filter(PurchaseOrder.status == args.status)

        if args.asin:
            query = query.filter(PurchaseOrder.asin == args.asin)

        orders = query.order_by(PurchaseOrder.order_date.desc()).all()

        if not orders:
            logger.info("No purchase orders found matching the criteria.")
            return

        logger.info(f"Found {len(orders)} purchase order(s):\n")

        header = f"{'PO ID':<30} {'ASIN':<12} {'Supplier':<10} {'Qty':>6} {'Unit Cost':>10} {'Total':>12} {'Status':<12} {'Expected Delivery':<20}"
        print(header)
        print("-" * len(header))

        for po in orders:
            supplier_name = ""
            if po.supplier:
                supplier_name = po.supplier.name[:10]
            print(
                f"{po.po_id:<30} "
                f"{po.asin:<12} "
                f"{supplier_name:<10} "
                f"{po.quantity:>6} "
                f"{format_currency(po.unit_cost):>10} "
                f"{format_currency(po.total_cost):>12} "
                f"{po.status:<12} "
                f"{format_date(po.expected_delivery):<20}"
            )

        print()
        logger.info(f"Total: {len(orders)} order(s)")
    finally:
        session.close()


