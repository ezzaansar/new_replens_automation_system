"""
Purchase Order Management Tool

Manages purchase orders for Amazon FBA Replens inventory replenishment.
Works with POs auto-generated by Phase 5 forecasting (--auto-po) or
manually created via this tool.

Supports:
  - List POs with filtering by status and ASIN
  - Create manual POs (picks best supplier with real costs)
  - Confirm, ship, receive, cancel POs
  - Receive updates inventory and supplier reliability
  - Summary statistics for PO pipeline

Usage:
  uv run python tools/manage_orders.py list                        # List all POs
  uv run python tools/manage_orders.py list --status pending       # Filter by status
  uv run python tools/manage_orders.py list --asin B0BF5W643L     # Filter by ASIN
  uv run python tools/manage_orders.py create B0BF5W643L 50       # Create PO for 50 units
  uv run python tools/manage_orders.py confirm PO-20260312-XXXX   # Confirm a PO
  uv run python tools/manage_orders.py ship PO-20260312-XXXX      # Mark PO as shipped
  uv run python tools/manage_orders.py receive PO-20260312-XXXX   # Mark PO as received
  uv run python tools/manage_orders.py cancel PO-20260312-XXXX    # Cancel a PO
  uv run python tools/manage_orders.py summary                    # PO pipeline statistics
"""

